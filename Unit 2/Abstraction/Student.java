public class Student {
    private int rollNo;
    private String name;

    public void  setRollNo(int rollNo){
        this.rollNo = rollNo;
    }

    public void setName(String name){
        this.name = name;
    }

    public int getRollNo(){
        return this.rollNo;
    }

    public String getName(){
        return this.name;
    }
    
}
